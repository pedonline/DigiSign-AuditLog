package bsm.dsal.digisign;

public class DigisignInformationExpiredException  extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	public DigisignInformationExpiredException() {
		super();
	}

	public DigisignInformationExpiredException(String arg0) {
		super(arg0);
	}

	public DigisignInformationExpiredException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DigisignInformationExpiredException(Throwable arg0) {
		super(arg0);
	}
}
